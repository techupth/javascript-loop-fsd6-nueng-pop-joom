// Exercise #4: Displaying Restaurant Name
let restaurants = ["KFC", "MOMO PARADISE", "Nabezo"];
let newRestaurants = [];
// Start coding here
for (let i = 0; i < restaurants.length ; i++) {
    newRestaurants[i] = restaurants[i];
}

for (let i = 0; i < newRestaurants.length ; i++) {

    console.log("Restaurant Name: " + newRestaurants[i]);
}
